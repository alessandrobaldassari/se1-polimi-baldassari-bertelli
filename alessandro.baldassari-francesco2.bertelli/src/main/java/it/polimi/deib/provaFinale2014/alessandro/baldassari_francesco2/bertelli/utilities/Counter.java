package it.polimi.deib.provaFinale2014.alessandro.baldassari_francesco2.bertelli.utilities;

public class Counter 
{

	private int value ;
	
	public Counter ( int initialValue ) 
	{
		value = initialValue ;
	}
	
	public void increment () 
	{
		value ++ ;
	}
	
	public int getValue () 
	{
		return value ;
	}
	
}
